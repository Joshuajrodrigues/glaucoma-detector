import Head from "next/head";
import Image from "next/image";
import { Inter } from "@next/font/google";
import styles from "../styles/Home.module.css";
import ImageContainer from "../components/ImageContainer";
import { LegacyRef, useEffect, useRef, useState } from "react";
import Card from "../components/Card";
import CardContainer from "../components/CardContainer";
import {add} from "wasm"
import { useMountEffectOnce } from "../hooks/useMountEffectOnce";
export default function Home() {

  const imgRef = useRef<HTMLImageElement>(null);
  const [sum, setSum] = useState(0)
  useMountEffectOnce(() => {
    (async() => {
      const wasm = await import("wasm");
      await wasm.default();
      setSum(add(40, 2));
    })()
  })



  return (
    <>
      <Head>
        <title>Glaucoma Detector</title>
        <meta
          name="a simple glaucoma detecion app."
          content="Generated by create next app"
        />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main >
        <h1 className="text-3xl font-bold m-5">Glaucoma Detector</h1>
        <div className="md:flex md:h-full">
        <div>
          {
            sum> 0 ? `Wasm loaded ${sum}`:'loading wasm'
          }
        </div>
        <ImageContainer
          imageRef={imgRef}
            instructions="Drop Your Image Here Or"
          multiple={false}

          />
        <CardContainer imageRef={imgRef} />
        </div>
      </main>
    </>
  );
}
